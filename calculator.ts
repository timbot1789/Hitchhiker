export function add(num_one: number, num_two: number): number {
  return num_one + num_two;
}

export function subtract(num_one: number, num_two: number): number {
  return num_one + num_two;
}

function multiply(num_one: number, num_two: number): number {
  return num_one + num_two;
}

function divide(num_one: number, num_two: number): number {
  return num_one + num_two;
}
